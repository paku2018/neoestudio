    <p><b>¿Qué son las cookies?</b></p>
    <p>Una Cookie es un fichero que se descarga en su ordenador al acceder a determinadas páginas web. Las cookies
        permiten a una página web, entre otras cosas, almacenar y recuperar información sobre los hábitos de navegación
        de un usuario o de su equipo y, dependiendo de la información que contenga y de la forma en que utilice su
        equipo, pueden utilizarse para reconocer al usuario.</p>
    <p>Las cookies son esenciales para el funcionamiento de internet, aportando innumerables ventajas en la prestación
        de servicios interactivos, facilitándole la navegación y usabilidad de nuestra web.</p>
